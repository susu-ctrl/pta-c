#include <stdio.h>  
  
int main() {  
    int s, m, n, k = 0;  
    scanf("%d %d %d", &s, &m, &n);  
  
    while (s > 0) {
        k++;  // 天数增加，放在这里是因为我们想要的是库存变为0或之前的那一天  
        s -= m; // 上午出货  
        if (s <= 0) break; // 如果上午出货后库存就小于等于0，则跳出循环  
        s += n; // 下午进货  
       
    }  
  
    // 如果s小于等于0，那么k就是我们要找的天数（因为我们在库存变为0或负数之前退出了循环）  
    // 如果s仍然大于0，那么说明下午进货后库存仍然为正，但这种情况在题目条件下不会发生  
    // 因为s, m, n的关系保证了在某个时刻库存会变为0或负数  
    printf("%d\n", k);  
  
    return 0;  
}
